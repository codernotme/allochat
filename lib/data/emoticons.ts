const EMOTICON_TOKEN_REGEX = /:([a-z0-9_]+):/gi;

export function findEmoticons(content: string): Array<{ token: string; index: number; length: number }> {
  const matches: Array<{ token: string; index: number; length: number }> = [];
  for (const match of content.matchAll(EMOTICON_TOKEN_REGEX)) {
    const token = (match[0] || "").toLowerCase();
    const index = match.index ?? -1;
    if (index >= 0 && EMOTICON_MAP[token]) {
      matches.push({ token, index, length: token.length });
    }
  }
  return matches;
}

export function hasEmoticons(content: string): boolean {
  return findEmoticons(content).length > 0;
}
